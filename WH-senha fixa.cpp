#include <iostream>
using namespace std;

	int main ()
	{
		
		int pin;
		
		do {
			cin >> pin;
			
			if (pin != 2002)
			{
				cout << "Senha Invalida" << endl;
			}
			
			else if (pin = 2002)
			{
				cout << "Acesso Permitido" << endl;
			}	
			
		} while (pin != 2002);

			
		return 0;
	}